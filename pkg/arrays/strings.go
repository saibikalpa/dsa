package arrays

import "strings"

func IsPalindrome(str string) bool {

	str = strings.ToLower(str)
	for i, j := 0, len(str)-1; i < j; i, j = i+1, j-1 {
		if str[i] != str[j] {
			return false
		}
	}
	return true
}

func Reverse(str string) string {
	arr := []rune(str)
	for i, j := 0, len(arr)-1; i < j; i, j = i+1, j-1 {
		arr[i], arr[j] = arr[j], arr[i]
	}
	return string(arr)
}
